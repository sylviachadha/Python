# if condition:
#   code .............
# This condition will return boolean data type i.e. either True or False

# In case condition returned True, inside code will be executed
if True:
    print("It worked")

# In case condition returned False, inside code will not be executed
if False:
    print("It worked")

#--------------------------------------------------------
    
num1 = 100
num2 = 200

if num1 > num2:
    print("num1 is greater")
else:    # implies every other case except the if statement case of num1>num2,
           # so even if they are equal it will say num2 is greater
    print("num2 is greater")


